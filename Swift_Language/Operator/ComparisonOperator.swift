class ComparisonOperator {
    func comparisonOperator() {
        let myScore: Int = 85
        let passingScore: Int = 70
        
        print("\(myScore > passingScore)\n\(myScore < passingScore)\n\(myScore == passingScore)\n\(myScore != passingScore)\n")
    }
}
