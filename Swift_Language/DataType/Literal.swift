class Literal {
    func printLiteral() {
        let name = "홍길동"
        let name2 = "홍길동2"
        let _123 = 456
        
        print("name은 String Literal")
        print("name은 \(name)")
        print("name2는 \(name2)")
        print("\(_123)의 변수명은 _123\n")
    }
}
